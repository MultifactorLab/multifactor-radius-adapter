    public static byte[] MakeKey(byte[] ntResponse, string password, bool isSend)
    {
        if (ntResponse.Length != 24)
           throw new ArgumentException("ntResponse must be 24 bytes in size");

        var ucs2Password = Encoding.Unicode.GetBytes(password);
        var passwordHash = Rfc2759.NTPasswordHash(ucs2Password);
        var passwordHashHash = Rfc2759.NTPasswordHash(passwordHash);
        var masterKey = GetMasterKey(passwordHashHash, ntResponse);

        return GetAsymmetricStartKey(masterKey, 16, isSend);
    }

    public static byte[] GetMasterKey(byte[] passwordHashHash, byte[] ntResponse)
    {
        using SHA1 sha = SHA1.Create();
        var data = new List<byte>();
        data.AddRange(passwordHashHash);
        data.AddRange(ntResponse);
        data.AddRange(_magic1);
        var hash = sha.ComputeHash(data.ToArray());
        return hash[..16];
    }

    public static byte[] GetAsymmetricStartKey(byte[] masterKey, int keyLength, bool isSend)
    {
        if (masterKey.Length != 16)
            throw new ArgumentException("masterKey must be 16 bytes in size");
        var data = new List<byte>();
        data.AddRange(masterKey);
        data.AddRange(_shaPad1);

        data.AddRange(isSend ? _magic3 : _magic2);
        data.AddRange(_shaPad2);
        using SHA1 sha = SHA1.Create();
        var hash = sha.ComputeHash(data.ToArray());
       
        return hash[..keyLength];
    }
