using Multifactor.Radius.Adapter.v2.Core.FirstFactor.MsChapV2;

namespace Multifactor.Radius.Adapter.v2.Tests.Unit.MsChapV2;

public class Rfc3079Tests
{
    [Fact]
    public void GetMasterKey()
    {
        var passwordHashHash = new byte[] { 0x41, 0xC0, 0x0C, 0x58, 0x4B, 0xD2, 0xD9, 0x1C, 0x40, 0x17, 0xA2, 0xA1, 0x2F, 0xA5, 0x9F, 0x3F };
        var ntResponse = new byte[] { 0x82, 0x30, 0x9E, 0xCD, 0x8D, 0x70, 0x8B, 0x5E, 0xA0, 0x8F, 0xAA, 0x39, 0x81, 0xCD, 0x83, 0x54, 0x42, 0x33, 0x11, 0x4A, 0x3D, 0x85, 0xD6, 0xDF };
        var expected = new byte[] { 0xFD, 0xEC, 0xE3, 0x71, 0x7A, 0x8C, 0x83, 0x8C, 0xB3, 0x88, 0xE5, 0x27, 0xAE, 0x3C, 0xDD, 0x31 };
        var result = Rfc3079.GetMasterKey(passwordHashHash, ntResponse);
        Assert.True(expected.SequenceEqual(result));
    }

    [Fact]
    public void GetAsymmetricStartKey()
    {
        var masterKey = new byte[] { 0xFD, 0xEC, 0xE3, 0x71, 0x7A, 0x8C, 0x83, 0x8C, 0xB3, 0x88, 0xE5, 0x27, 0xAE, 0x3C, 0xDD, 0x31 };
        var sessionKeyLength = 8;
        var isSend = true;
        var expected = new byte[] { 0x8B, 0x7C, 0xDC, 0x14, 0x9B, 0x99, 0x3A, 0x1B };
        var result = Rfc3079.GetAsymmetricStartKey(masterKey, sessionKeyLength, isSend);
        Assert.True(expected.SequenceEqual(result));
    }
    
    [Fact]
    public void GetAsymmetricStartKey2()
    {
        var masterKey = new byte[] { 0xFD, 0xEC, 0xE3, 0x71, 0x7A, 0x8C, 0x83, 0x8C, 0xB3, 0x88, 0xE5, 0x27, 0xAE, 0x3C, 0xDD, 0x31 };
        var sessionKeyLength = 16;
        var isSend = true;
        var expected = new byte[] { 0x8B, 0x7C, 0xDC, 0x14, 0x9B, 0x99, 0x3A, 0x1B, 0xA1, 0x18, 0xCB, 0x15, 0x3F, 0x56, 0xDC, 0xCB };
        var result = Rfc3079.GetAsymmetricStartKey(masterKey, sessionKeyLength, isSend);
        Assert.True(expected.SequenceEqual(result));
    }
}