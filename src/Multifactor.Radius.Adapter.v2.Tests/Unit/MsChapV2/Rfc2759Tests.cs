    [Fact]
    public void GenerateNTResponse_ShouldGenerateNTResponse()
    {
        var authenticatorChallenge = new byte[] { 0x77, 0xac, 0x2d, 0x4c, 0x31, 0x2a, 0x6a, 0xfe, 0xb9, 0xd1, 0x76, 0xb4, 0xdd, 0x1d, 0x1a, 0x1d};
        var peerChallenge = new byte[] { 0x34, 0x13, 0x16, 0x83, 0x81, 0xf7, 0x4b, 0x7b, 0x28, 0xe6, 0x08, 0x8b, 0xd7, 0xa5, 0x0d, 0xe9 };
        var username = "test";
        var password = "superSecretPassword";

        var expected = new byte[] { 0x62, 0x95, 0xb2, 0x14, 0x39, 0x95, 0xf9, 0xf6, 0x58, 0x69, 0x19, 0x77, 0xef, 0x12, 0x79, 0x89, 0x10, 0xff, 0x29, 0x73, 0xb5, 0xb5, 0x13, 0xba };
        var result = Rfc2759.GenerateNTResponse(authenticatorChallenge, peerChallenge, username, password);
        Assert.True(expected.SequenceEqual(result));
    }
    
