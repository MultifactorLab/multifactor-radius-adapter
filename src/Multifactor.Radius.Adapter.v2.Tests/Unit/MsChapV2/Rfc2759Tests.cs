    [Fact]
    public void ChallengeHash_ShouldCalculateChallengeHash()
    {
        var authenticatorChallenge = new byte[] { 0x77, 0xac, 0x2d, 0x4c, 0x31, 0x2a, 0x6a, 0xfe, 0xb9, 0xd1, 0x76, 0xb4, 0xdd, 0x1d, 0x1a, 0x1d };
        var peerChallenge = new byte[] { 0x34, 0x13, 0x16, 0x83, 0x81, 0xf7, 0x4b, 0x7b, 0x28, 0xe6, 0x08, 0x8b, 0xd7, 0xa5, 0x0d, 0xe9 };
        var username = "test";
        
        var sha1Hash = Rfc2759.ChallengeHash(authenticatorChallenge, peerChallenge, username);
        var expected = new byte[] { 75, 54, 203, 169, 26, 155, 168, 104 };
        Assert.True(expected.SequenceEqual(sha1Hash));
    }
    
