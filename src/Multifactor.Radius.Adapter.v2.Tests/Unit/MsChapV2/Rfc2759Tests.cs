    [Fact]
    public void PadKey_ShouldPadKey()
    {
        var key = new byte[] { 0x61, 0xee, 0x8b, 0x50, 0x74, 0x8f, 0x5e };
        var expected = new byte[] { 0x61, 0xf7, 0xa2, 0x6b, 0x07, 0xa4, 0x3d, 0xbc};
        var result = Rfc2759.ParityPadDESKey(key);
